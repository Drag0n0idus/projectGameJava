public class Enemy {
    private int dmg;
    private int health;
    private String name;

    public int getDmg() {
        return dmg;
    }

    public void setDmg(int dmg) {
        this.dmg = dmg;
    }

    public int getHealth() {
        return health;
    }

    public void setHealth(int health) {
        this.health = health;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    public String getHealthS() {
        return Integer.toString(this.getHealth());
    }
    
    
}
